public class Player {
    int nNumber;
    long lColor;
    boolean bWin;

    public int getnNumber() {
        return nNumber;
    }

    public long getlColor() {
        return lColor;
    }

    public boolean isbWin() {
        return bWin;
    }

    public void setNumber(int number) {
        this.nNumber = number;
    }

    public void setColor(long color) {
        this.lColor = color;
    }

    public void setWin(boolean win) {
        this.bWin = win;
    }
}
